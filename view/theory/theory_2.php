            <h2>Thang Âm và Giọng Điệu</h2>
            <p>Thang âm trưởng và thứ là nền tảng của âm nhạc. Thang âm trưởng mang lại cảm giác vui tươi, còn thang âm thứ tạo cảm giác u buồn, cảm xúc. Ngoài ra, còn có các thang âm khác như pentatonic, blues, và một số thang âm khác có ảnh hưởng lớn trong âm nhạc phương Tây và các nền văn hóa khác nhau.</p>

            <h3>Thang Âm Trưởng và Thứ</h3>
            <p>Thang âm trưởng được xây dựng từ chuỗi các nốt nhạc: <code>Do – Re – Mi – Fa – Sol – La – Si – Do</code>. Thang âm này mang lại cảm giác vui tươi, hạnh phúc và thịnh vượng. Thang âm thứ có các biến thể như tự nhiên, hòa thanh và melodic, mỗi biến thể tạo ra một sắc thái khác biệt, từ cảm giác u sầu đến tràn đầy cảm xúc. Cấu trúc thang âm thứ tự nhiên là: <code>Do – Re – Đố – Fa – Sol – La – Si – Do</code>, trong khi thang âm thứ hòa thanh có sự thay đổi ở nốt thứ 7: <code>Do – Re – Đố – Fa – Sol – La – Si# – Do</code>, tạo ra một âm thanh căng thẳng hơn.</p>

            <h4>Thang Âm Pentatonic và Blues</h4>
            <p>Thang âm pentatonic là một thang âm gồm 5 nốt, được sử dụng phổ biến trong nhạc dân gian và rock. Ví dụ của thang âm pentatonic trong Do trưởng là <code>Do – Re – Mi – Sol – La</code>. Thang âm blues là một biến thể của pentatonic với việc sử dụng các nốt "blue" (thường là b3, b5 và b7), tạo ra cảm giác ma mị, đượm buồn và đầy cảm xúc. Thang âm này rất phổ biến trong các thể loại nhạc blues, rock, jazz.</p>

            <h4>Thang Âm Lưỡng Tính (Chromatic Scale)</h4>
            <p>Thang âm lưỡng tính là thang âm chứa tất cả các nốt nhạc trong một quãng tám, mỗi nốt cách nhau một nửa cung. Thang âm này mang lại sự thay đổi sắc thái, tạo ra cảm giác căng thẳng và không ổn định, thường được sử dụng để tạo hiệu ứng trong các tác phẩm âm nhạc.</p>
