            <h2>Hợp Âm và Cấu Trúc Hợp Âm</h2>
            <p>Hợp âm 7, hợp âm 9 là các hợp âm phức tạp được sử dụng để tạo chiều sâu cho các tác phẩm âm nhạc. Cấu trúc hợp âm thường được xây dựng từ các quãng ba và quãng năm, với các hợp âm 3 nốt cơ bản là cấu trúc cơ bản nhất trong âm nhạc.</p>

            <h3>Hợp Âm 7 và 9</h3>
            <p>Hợp âm 7 bao gồm các nốt cơ bản và nốt thứ 7 (ví dụ: C7: Do, Mi, Sol, Sib). Hợp âm 9 có thêm nốt thứ 9 (ví dụ: C9: Do, Mi, Sol, Sib, Re). Hợp âm này thường được dùng để tạo ra sự căng thẳng và dẫn dắt đến giải quyết hợp âm tiếp theo trong một đoạn nhạc.</p>

            <h4>Chuyển Hợp Âm và Vòng Hòa Âm</h4>
            <p>Vòng hòa âm cơ bản I-IV-V-I là một cấu trúc quan trọng trong âm nhạc phương Tây, trong đó hợp âm I là hợp âm chủ, IV là hợp âm phụ và V là hợp âm dẫn dắt. Chuyển hợp âm có thể linh hoạt hơn với các hợp âm bậc thấp (ii, iii, vi), tạo ra các chuỗi hợp âm phức tạp và thú vị hơn.</p>
