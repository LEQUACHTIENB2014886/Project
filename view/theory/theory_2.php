            <h2>Điệu Thức và Modulations</h2>
            <p>Điệu thức giúp tạo ra cảm xúc đặc biệt cho bản nhạc. Có các điệu thức như Dorian, Phrygian, Mixolydian, cùng với việc chuyển điệu giữa các giọng, hay còn gọi là modulations.</p>

            <h3>Điệu Thức Dorian</h3>
            <p>Điệu thức Dorian là điệu thức thứ nhưng có nốt thứ 6 tự nhiên, không giảm như điệu thức thứ tự nhiên. Cấu trúc: <code>Do – Re – Đố – Fa – Sol – La – Si – Do</code>. Điệu thức Dorian mang lại một cảm giác vừa trầm tư, vừa có sự ổn định và nhẹ nhàng. Đây là điệu thức phổ biến trong nhạc jazz và blues.</p>

            <h3>Điệu Thức Phrygian</h3>
            <p>Điệu thức Phrygian có nốt thứ 2 giảm, tạo cảm giác u tối và kỳ bí. Cấu trúc: <code>Do – Đố – Re – Fa – Sol – La – Si – Do</code>. Điệu thức này thường được sử dụng trong các thể loại nhạc metal, đặc biệt là trong death metal và doom metal, để tạo ra sự u sầu và căng thẳng.</p>

            <h4>Điệu Thức Mixolydian</h4>
            <p>Điệu thức Mixolydian là điệu thức trưởng nhưng có nốt thứ 7 giảm, tạo ra một cảm giác mở và thoải mái. Cấu trúc của điệu thức Mixolydian là <code>Do – Re – Mi – Fa – Sol – La – Si♭ – Do</code>. Đây là điệu thức phổ biến trong nhạc blues, rock và jazz.</p>
